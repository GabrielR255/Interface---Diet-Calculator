<h1>O que é Gasto Calórico</h1>
<br>
    <p>O gasto calórico é a quantidade de calorias que uma pessoa queima diariamente em atividades básicas e atividades físicas. Entender o gasto calórico é fundamental para quem busca manter, perder ou ganhar peso, pois ele indica quantas calorias precisam ser consumidas ou evitadas para atingir esses objetivos.</p>
    <br>
    <h2>Como o Gasto Calórico é Calculado</h2>
    <br>
    <p>O cálculo do gasto calórico total considera fatores como taxa metabólica basal (TMB), que é a quantidade de calorias que o corpo consome em repouso para manter funções vitais como respiração e circulação sanguínea, além das calorias queimadas com atividades físicas e movimentos ao longo do dia.</p>
<br>
    <h2>Como Calcular o Gasto Calórico na Plataforma Diet Calculator</h2>
    <br>
    <p>A plataforma <strong>Diet Calculator</strong> oferece uma ferramenta prática para calcular o gasto calórico de forma personalizada. O usuário insere informações como idade, peso, altura, nível de atividade física e objetivo (como perda, manutenção ou ganho de peso). Com esses dados, a plataforma calcula automaticamente o gasto calórico diário, ajudando o usuário a entender melhor suas necessidades energéticas.</p>

    <p>Além disso, o Diet Calculator pode sugerir metas calóricas diárias e fornecer orientação para alcançar os objetivos desejados, facilitando o planejamento de refeições e o monitoramento da alimentação.</p>
