<h1>O que é IMC</h1>
<br>
    <p>O Índice de Massa Corporal (IMC) é uma medida internacionalmente reconhecida para avaliar se uma pessoa está dentro de um peso saudável em relação à sua altura. Essa métrica é amplamente usada por profissionais de saúde para indicar o grau de sobrepeso ou obesidade, ou, em casos contrários, identificar um peso muito abaixo do ideal, considerando os impactos na saúde.</p>
    <br>
    <h2>Como o IMC é Calculado</h2>
    <br>
    <p>Para calcular o IMC, basta dividir o peso (em quilogramas) pela altura (em metros) elevada ao quadrado. A fórmula é a seguinte:</p>
   <br>
    <p><strong>IMC = peso (kg) / altura (m)²</strong></p>
<br>
    <h2>Como Calcular o IMC na Plataforma Diet Calculator</h2>
    <br>
    <p>A plataforma <strong>Diet Calculator</strong> oferece uma maneira fácil e prática de calcular o IMC. Ao acessar a ferramenta, você insere seus dados de peso e altura nos campos apropriados, e o sistema automaticamente realiza o cálculo com base na fórmula do IMC. Após o cálculo, a plataforma apresenta os resultados e, muitas vezes, inclui uma faixa de classificação para indicar se o IMC está dentro dos limites considerados saudáveis, sobrepeso ou obesidade.</p>
